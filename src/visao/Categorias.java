    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(cxtCat.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Digite um nome para a categoria !");
            cxtCat.requestFocus();
        }else{
            mod.setCategoria(cxtCat.getText());
            mod.setDescricao(cxtDesc.getText());
            control.cadastraCat(mod);
            preenchetabelaCategorias("select * from tbl_categorias order by nome_cat");
            
            cxtCat.setText("");
            cxtDesc.setText("");
            cxtCat.setEnabled(false);
            cxtDesc.setEnabled(false);

            btnNovo.setEnabled(true);
            btnSalvar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnAlterar.setEnabled(false);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int alterar=JOptionPane.showConfirmDialog(null, "Tem certeza que deseja alterar os dados da categoria selecionada ?","Atenção",JOptionPane.YES_NO_OPTION);
        if(alterar==JOptionPane.YES_OPTION){
            btnNovo.setEnabled(true);
            btnSalvar.setEnabled(false);
            btnExcluir.setEnabled(true);
            btnAlterar.setEnabled(true);
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int excluir=JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir essa categoria?","Atenção",JOptionPane.YES_NO_OPTION);
        if(excluir==JOptionPane.YES_OPTION){       
            cxtCat.setText("");
            cxtDesc.setText("");
            cxtCat.setEnabled(false);
            cxtDesc.setEnabled(false);

            btnNovo.setEnabled(true);
            btnSalvar.setEnabled(false);
            btnExcluir.setEnabled(false);
            btnAlterar.setEnabled(false);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

