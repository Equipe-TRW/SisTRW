    public void preenchetabelaCategorias(String sql){
        ArrayList dados=new ArrayList();
            //VETOR COLUNAS COM OS CABEÇALHOS
            String[] colunas=new String[]{"ID","Categoria","Descrição"};
            //ABRINDO CONEXÃO
            conectaCat.conexao();
            //CRIANDO UMA CONSULTA SQL
            conectaCat.executaSQL(sql);
            try {
                //SETANDO O RESULTSET DA CONSULTA NA PRIMEIRA LINHA
                conectaCat.rs.first();
                //LOOP QUE VAI ALIMENTAR MEU VETOR DE OBJETO COM TODOS OS REGISTROS DA STRING SQL PASSADO ACIMA
                do{
                    dados.add(new Object[]{conectaCat.rs.getInt("id_Cat"),conectaCat.rs.getString("nome_Cat"),conectaCat.rs.getString("desc_cat")});                      
                //ENQUANTO EXISTIR REGISTRO PULE PARA A PROXIMA LINHA
                }while(conectaCat.rs.next());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao preencher Tabela!"+e);
            }
            conectaCat.desconecta();
            modeloTabela tabela=new modeloTabela(dados, colunas);
            
            tabelaCategorias.setModel(tabela);
            tabelaCategorias.getColumnModel().getColumn(0).setPreferredWidth(40);
            tabelaCategorias.getColumnModel().getColumn(0).setResizable(true);
            tabelaCategorias.getColumnModel().getColumn(1).setPreferredWidth(150);
            tabelaCategorias.getColumnModel().getColumn(1).setResizable(true);
            tabelaCategorias.getColumnModel().getColumn(2).setPreferredWidth(110);
            tabelaCategorias.getColumnModel().getColumn(2).setResizable(true);
            
            tabelaCategorias.getTableHeader().setReorderingAllowed(false);
            tabelaCategorias.setAutoResizeMode(tabelaCategorias.AUTO_RESIZE_OFF);
            tabelaCategorias.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);   
    }
    
